function getFocusKey(day: number): string {
  const focuses = [
    'focus1', 'focus2', 'focus3', 'focus4', 'focus5',
    'focus6', 'focus7', 'focus8', 'focus9', 'focus10',
    'focus11', 'focus12', 'focus13', 'focus14', 'focus15',
    'focus16', 'focus17', 'focus18', 'focus19', 'focus20',
    'focus21', 'focus22', 'focus23', 'focus24', 'focus25',
    'focus26', 'focus27', 'focus28', 'focus29', 'focus30',
  ];
  return focuses[day - 1] || 'focus1';
}

